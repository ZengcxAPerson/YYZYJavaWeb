package com.cmbb.bean;

public class StockRegion {
	private int StockRegionId;
	private String StockRegionNumber;
	private String StockRegionName;
	private String StockNumber;
	
	public String getStockNumber() {
		return StockNumber;
	}
	public void setStockNumber(String stockNumber) {
		StockNumber = stockNumber;
	}
	public int getStockRegionId() {
		return StockRegionId;
	}
	public void setStockRegionId(int stockRegionId) {
		StockRegionId = stockRegionId;
	}
	public String getStockRegionNumber() {
		return StockRegionNumber;
	}
	public void setStockRegionNumber(String stockRegionNumber) {
		StockRegionNumber = stockRegionNumber;
	}
	public String getStockRegionName() {
		return StockRegionName;
	}
	public void setStockRegionName(String stockRegionName) {
		StockRegionName = stockRegionName;
	}
	
	

}
