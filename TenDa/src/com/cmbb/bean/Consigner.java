package com.cmbb.bean;

public class Consigner {
	private int ConsignerId ;
	private String ConsignerNumber ;
	private String ConsignerCompany ;
	private String ConsignerName ;
	private String ConsignerAddr ;
	private String ConsignerPhone ;
	private String ConsignerMobile ;
	private String ConsignerFax ;
	private String ConsignerEmail ;
	private String ConsignerIdCard ;
	public int getConsignerId() {
		return ConsignerId;
	}
	public void setConsignerId(int consignerId) {
		ConsignerId = consignerId;
	}
	public String getConsignerNumber() {
		return ConsignerNumber;
	}
	public void setConsignerNumber(String consignerNumber) {
		ConsignerNumber = consignerNumber;
	}
	public String getConsignerCompany() {
		return ConsignerCompany;
	}
	public void setConsignerCompany(String consignerCompany) {
		ConsignerCompany = consignerCompany;
	}
	public String getConsignerName() {
		return ConsignerName;
	}
	public void setConsignerName(String consignerName) {
		ConsignerName = consignerName;
	}
	public String getConsignerAddr() {
		return ConsignerAddr;
	}
	public void setConsignerAddr(String consignerAddr) {
		ConsignerAddr = consignerAddr;
	}
	public String getConsignerPhone() {
		return ConsignerPhone;
	}
	public void setConsignerPhone(String consignerPhone) {
		ConsignerPhone = consignerPhone;
	}
	public String getConsignerMobile() {
		return ConsignerMobile;
	}
	public void setConsignerMobile(String consignerMobile) {
		ConsignerMobile = consignerMobile;
	}
	public String getConsignerFax() {
		return ConsignerFax;
	}
	public void setConsignerFax(String consignerFax) {
		ConsignerFax = consignerFax;
	}
	public String getConsignerEmail() {
		return ConsignerEmail;
	}
	public void setConsignerEmail(String consignerEmail) {
		ConsignerEmail = consignerEmail;
	}
	public String getConsignerIdCard() {
		return ConsignerIdCard;
	}
	public void setConsignerIdCard(String consignerIdCard) {
		ConsignerIdCard = consignerIdCard;
	}
	
}
